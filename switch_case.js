//calculator by switch statment
let op = "+"
let a=9
let b=6
switch(op){
    case "*":
        console.log(a*b)
        break
    case "+":
        console.log(a+b)
        break
    case "-":
        console.log(a-b)
        break
    case "/":
        console.log(a/b)
        break
    default:
        console.log("invaled")
}